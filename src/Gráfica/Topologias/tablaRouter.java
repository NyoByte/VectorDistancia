    public tablaRouter(int n,int[][] distancias, int[][] saltos) {
        this.distancias = distancias;
        this.saltos = saltos;
        this.tam = distancias.length;
        initComponents();
        modelo = new DefaultTableModel();
        modelo.addColumn("Hacia router");
        modelo.addColumn("Distancia");
        modelo.addColumn("Siguiente salto");
        jTable1.setModel(modelo);
        this.MostrarTabla(n);
        this.setLocationRelativeTo(null);
    }
    
    public void MostrarTabla(int n){
        this.jLabel2.setText("Router " + n);
        while(modelo.getRowCount()>0){
            modelo.removeRow(0);
        }
        String[] fila = new String[3];
        for(int i=0; i<tam; i++){
            fila[0] = String.valueOf(i);
            fila[1] = String.valueOf(distancias[n][i]);
            fila[2] = String.valueOf(saltos[n][i]);
            modelo.addRow(fila);
        }
        this.jTable1.setModel(modelo);
    }

